def hablarConSabio():
    global hablandoConSabio, respuesta, misionSabioActiva, puede_hablar_herrero
    if misionSabioActiva == False:
        if hablandoConSabio == 0:
            game.show_long_text("Sabio Aleron: ¡Hola! Perdí mi diario.", DialogLayout.BOTTOM)
            hablandoConSabio = 1
        elif hablandoConSabio == 1:
            game.show_long_text("Sabio: ¿Me ayudas a buscarlo?", DialogLayout.BOTTOM)
            hablandoConSabio = 2
        elif hablandoConSabio == 2:
            respuesta = game.ask("SI", "NO")
            if respuesta:
                misionSabioActiva = True
                hablandoConSabio = 3
                crearDiario()
            else:
                game.show_long_text("Sabio: Tal vez otro día...", DialogLayout.BOTTOM)
                hablandoConSabio = 4
        elif hablandoConSabio == 3:
            game.show_long_text("Sabio: ¡Gracias! Está en el bosque.", DialogLayout.BOTTOM)
        else:
            if hablandoConSabio == 4:
                game.show_long_text("Sabio: ¿Has cambiado de opinión?", DialogLayout.BOTTOM)
                hablandoConSabio = 2
            if hablandoConSabio == 5:
                game.show_long_text("Sabio: Gracias nuevamente por tu ayuda.",
                    DialogLayout.BOTTOM)
    elif diarioEncontrado == True:
        game.show_long_text("Sabio: ¡Lo encontrase!", DialogLayout.BOTTOM)
        misionSabioActiva = False
        hablandoConSabio = 5
        pause(500)
        game.show_long_text("Sabio: Este diario... habla de mi maestro.",
            DialogLayout.BOTTOM)
        pause(500)
        game.show_long_text("El Alquimista Valerio desapareció hace años...",
            DialogLayout.BOTTOM)
        pause(500)
        game.show_long_text("El Alquimista dejó tres llaves ocultas.",
            DialogLayout.BOTTOM)
        pause(500)
        game.show_long_text("La primera está con el herrero Bron.", DialogLayout.BOTTOM)
        pause(500)
        game.show_long_text("Ve a verlo. Está al otro lado del pueblo.",
            DialogLayout.BOTTOM)
        puede_hablar_herrero = True
    elif randint(0, 100) < 50:
        game.show_long_text("Sabio: ¿Ya miraste al fondo del bosque?",
            DialogLayout.BOTTOM)
        pause(800)
        game.show_long_text("Donde están los robles más grandes...", DialogLayout.BOTTOM)
        pause(800)
        game.show_long_text("Allí debe estar.", DialogLayout.BOTTOM)
    else:
        game.show_long_text("Sabio: ¿Ya lo encontrase?", DialogLayout.BOTTOM)
