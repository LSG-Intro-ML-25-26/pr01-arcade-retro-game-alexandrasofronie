def on_on_overlap(sprite, otherSprite):
    if otherSprite == sabio:
        hablarConSabio()
    elif otherSprite == herrero:
        hablarConHerrero()
sprites.on_overlap(SpriteKind.player, SpriteKind.NPC, on_on_overlap)

